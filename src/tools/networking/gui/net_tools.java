    void writetofile(String[] info, String[][] text){
        
        try{ //Create file
            
            File outputf = new File(extraction_path+"\\subnets.txt");
            outputf.createNewFile();

        }catch(Exception e){
            error_jlabel.setText("Error Creating file");
        }
        
        error_jlabel.setText("Writing to file...");
        
        try{//write to file
            FileWriter wr = new FileWriter(extraction_path+"\\subnets.txt");
            
            wr.write("Mask Bits : "+ info[0]+"\n");
            wr.write("Host Bits : "+ info[1]+"\n");
            wr.write("Number of Subnets : "+ info[3]+"\n");
            wr.write("Hosts per Subnet : "+ info[4]+"\n");
            wr.write("Subnet Bit Mask : "+ info[5]+"\n");
            
            wr.write("\n  \t Subnet\t\t\t\t Host Range\t\t\t\t Broadcast\n");
            
            for(int i=0; i< Integer.parseInt(info[3]); i++)
                wr.write((i+1) + "\t" + text[i][0] + "\t\t\t" + text[i][1] + "\t to \t" + text[i][2] + "\t\t\t" + text[i][3] + "\n");

            wr.close();
            
        }catch(Exception e){
            error_jlabel.setText("ERROR while writing to file");
        }
        
        error_jlabel.setText("File exported!");
    }
    
    void getexpath(){ //get extraction path
        try{
            JFileChooser ch = new JFileChooser();
            ch.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            ch.showOpenDialog(null);
            File f = ch.getSelectedFile();
            extraction_path = f.getAbsolutePath();
        }catch(Exception e){
        }
    }
    
    void settextarea(String[] info, String[][] text){
        subnet_textarea.setText("");
        for(int i = 0; i< Integer.parseInt(info[3]); i++ ){
            subnet_textarea.append((i+1) + "\t" + text[i][0] + "\t\t\t" + text[i][1] + "\t to \t" + text[i][2] + "\t\t\t" + text[i][3] + "\n");
        }
    }
    
    void setinfotextfields(String[] info){
        masbits_textfield.setText(info[0]);
        hostbits_textfield.setText(info[1]);
        subnets_textfield.setText(info[3]);
        hosts_textfield.setText(info[4]);
        bitmask_textfield.setText(info[5]);
    }
    
    void error(String code){
        // # 1 = wrong ip size
        // # 2 = wrong mask size
        // # 3 = out of boundaries ip
        // # 4 = out of boundaries mask
        // # 5 = mask format wrong
        if(code.equals("1"))
            error_jlabel.setText("Wrong Size of IP");
        else if(code.equals("2"))
            error_jlabel.setText("Wrong Size of Mask");
        else if(code.equals("3"))
            error_jlabel.setText("IP out of Boundaries");
        else if(code.equals("4"))
            error_jlabel.setText("Mask out of Boundaries");
        else if(code.equals("5"))
            error_jlabel.setText("Wrong Mask Format");
        else if (code.equals("6"))
            error_jlabel.setText("IP and Mask Classes don't match");
        else
            error_jlabel.setText("Error");
        
    }

