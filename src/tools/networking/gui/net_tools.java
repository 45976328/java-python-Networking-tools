    private void generate_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generate_buttonActionPerformed
        String ip = ip_textfield.getText();
        String mask = mask_textfield.getText();
        String code;
        
        error_jlabel.setText("");
        
        classfull_subnetting c = new classfull_subnetting(ip,mask);
        code = c.check();

        if (code.equals("0")){
            info = c.getinfo();
            
            setinfotextfields(info);
            
            if(Integer.parseInt(info[3]) > 65536){
                error_jlabel.setText("Too many subnets to print, try exporting");
                subnet_textarea.setText("");
            }else{
                subnets=c.gen();
                settextarea(info,subnets);
            }
        }else{
            error(code);
        }
    }//GEN-LAST:event_generate_buttonActionPerformed

    private void export_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_export_buttonActionPerformed
        String ip = ip_textfield.getText();
        String mask = mask_textfield.getText();
        String code;


        error_jlabel.setText("");

        classfull_subnetting c = new classfull_subnetting(ip,mask);
        code = c.check();
        
        if (code.equals("0")){
            
            if (extraction_path == null) //check if path is empty
                getexpath(); //get extraction path

           if( info == null || Integer.parseInt(info[3]) > 65636 ){
               
               error_jlabel.setText("Exporting...");
               c.writer(extraction_path);

               error_jlabel.setText("File exported!");
               
               subnet_textarea.setText("");
               masbits_textfield.setText("");
               hostbits_textfield.setText("");
               subnets_textfield.setText("");
               hosts_textfield.setText("");
               bitmask_textfield.setText("");
                             
           }else{
               writetofile(info,subnets);
           }
           
        }else{
            error(code);
        }
    }//GEN-LAST:event_export_buttonActionPerformed

